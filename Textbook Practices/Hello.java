class americanDate {
    public static void main(String[] args) {
        //setting variables for day, date, month, and year
        String greetings = "Today's date is ";
        String day = "Wednesday, ";
        int date = 1;
        String month = "June ";
        int year = 2022;
        //print everything out in order
        System.out.print(greetings);
        System.out.print(day);
        System.out.print(month);
        System.out.print(date);
        System.out.print(",");
        System.out.print(" ");
        System.out.println(year);
    }
}

